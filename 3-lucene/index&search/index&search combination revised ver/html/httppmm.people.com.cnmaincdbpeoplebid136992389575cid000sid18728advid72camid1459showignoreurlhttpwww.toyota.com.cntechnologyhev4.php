        	<p><strong>全速行驶时，同时使用发动机和电动机</strong><br />蓄电池的电力供给到电动机以提供辅助动力，通过结合电动机和发动机的动力，丰田油电混合双擎动力车提供的动力能够和拥有更大发动机排量的汽车相媲美。</p>
        	<p><strong>减速／制动能量再充电</strong><br />当制动或松开油门时，车的动能通过车轮带动电动机和发电机，将车辆的动能转化为电能并储存在蓄电池中。</p>
