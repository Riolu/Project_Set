      	<h1>丰田油电混合双擎动力系统（THS）的工作原理</h1>
        <p><strong>丰田油电混合动力系统<span>（THS）</span>为混合动力系统设立了一个全新的标准</strong><br /><br />具备出色燃油经济性的混联式混合动力系统<br />
丰田油电混合动力系统将电动机和发动机动力进行优化匹配，在降低油耗的同时提供出色的驾驶性能。<br />
丰田在全方位推进包括混合动力车在内的环保车型发展的同时，还把混合动力技术定位为蕴含了开发各种环保车所必需的技术要素的关键技术，既可以节约石油，又为最终摆脱石油打下了基础。<br />
<span><sup>＊</sup>THS：Toyota Hybrid System</span></p>
		
        <div class="liuc_01 liuc">
        	<p><strong>当汽车处于静止状态时，会关闭整个动力系统。</strong><br />当汽车变成静止时，发动机、电动机和发电机会自动关闭，避免因怠速造成的能量浪费。</p>
        	<p><strong>启动时仅使用电动机。</strong><br />丰田油电混合双擎动力车在启动时只需要使用电池带动电动机即可。</p>
        	<p class="noMr"><strong>发动机作为主动力源，电动机辅助驱动。</strong><br />发动机的动力直接驱动车轮，并根据驾驶条件将一部分动力用于为蓄电池充电或由电动机介入来辅助发动机驱动。</p>
        </div>
        
