                                <h1>聚沙成塔  助力母校</h1>
                                <h2 align="center"><b>思源校友年度捐赠简介</b></h2>
                                			   &nbsp;&nbsp;&nbsp;&nbsp;将母校建设成为世界一流大学，是全体交大人的共同愿望，也是全体交大人共同的事业。在国际公认的世界一流大学评估体系中，校友年度捐赠率在大学排名中所占比重达5%，是衡量一所大学人才培养水平的重要指标。“思源校友年度捐赠”是上海交通大学校友总会为弘扬"饮水思源、爱国荣校" 的光荣传统，助力母校成为世界一流大学而设立的一项长期计划。项目自2004年启动至今已届十年，这项由校友倡导、校友出资、校友管理的活动，从萌芽至壮大，影响日深，传播渐广。，在帮助母校人才培养、扶持学科建设、改善校园环境等方面起到了越来越重要的作用，使在校师生深深感受到海内外校友们的殷殷呵护，也赋予了莘莘学子们责任与担当，激励着我们把这份“饮水思源”之情代代传承。
<br /> &nbsp;&nbsp;&nbsp;&nbsp;所有“思源校友年度捐赠”参与者的姓名和金额将登记造册，载入校史档案，作为永久留念。
