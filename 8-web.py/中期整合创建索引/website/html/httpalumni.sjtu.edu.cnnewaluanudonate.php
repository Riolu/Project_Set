								<div class="ahead">
                               
								<h2><strong>一、思源基金  （非限定用途）</strong></h2>
								</div>
								<div class="zw">
								<div class="wordsout">
								<div class="word_s" style="font-size:12px">
                                <br />&nbsp;&nbsp;&nbsp;&nbsp;落其实者思其树，饮其流者怀其源。这既作为交大人共同诠释的一种精神烙印，更是引导守望的归属感，使我们的感情深深扎根在这个我们无比热爱的交大家园中。我们积极倡导校友奉献爱心，践行“饮水思源，爱国荣校”的校训，让更多需要关注的群体获得帮助，我们更承诺将认真用好每一分捐赠款项，让校友的爱心遍布母校。 <br />
&nbsp;&nbsp;&nbsp;&nbsp;<strong>捐赠说明</strong>：本项目起捐金额为人民币 30 元，所有捐赠款项将汇入思源基金，作为学校发展的非限定基金使用。该基金主要用于学校科研与师资发展、学生奖助学金的设立和校园相关设施配备等方面。母校延续多年的 “校友爱心助学金”便是用其中部分款项设立，每年资助60名品学兼优但家境暂时困难的在校学生，在广大师生中有着良好的反响。
