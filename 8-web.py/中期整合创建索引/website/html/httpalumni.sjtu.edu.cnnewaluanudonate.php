								<h2><strong>二、冠名项目</strong></h2>
								</div>
								<div class="zw">
								<div class="wordsout">
								<div class="word_s" style="font-size:12px">
                                <br />&nbsp;&nbsp;&nbsp;&nbsp;百余年光阴如白驹过隙匆匆流过，无数交大学子带着母校给予的翅膀去搏击长空，迎接风雨，母校依然以温暖而骄傲的目光关注着每一位游子的拼搏与努力，从未远离。悠悠寸草心，报得三春晖，让我们把最美的记忆寄托在菁菁校园中，愿巍峨校门永远闪耀光辉，愿母校之树更加根深叶茂。母校把闵行校区的三处标志性景观――闵行校区东大门、 新体育馆座椅、校友林作为年度捐赠的冠名项目向广大校友推出，所筹集资金将用于校园环境建设与维护。  <br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;<strong>捐赠说明：</strong><br /><br />
<strong>&nbsp;&nbsp;&nbsp;&nbsp;体育馆座椅认捐</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;上海交通大学新体育馆座椅普通席认捐额每人每席1000元人民币起，学校将镌刻有认捐者信息（姓名、毕业年、院系专业）的纪念座位牌镶嵌于椅背醒目处以示纪念。从建校110周年开始，已获认捐的体育馆座椅共有497个。<br /><br />

     <strong>&nbsp;&nbsp;&nbsp;&nbsp;校友林树木认养</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;校友林位于闵行校区宣怀大道和思源东路交叉路口，应届、往届毕业生均可参与校友林树木的认养。个人捐赠3000元人民币，班级捐赠1万元人民币，可在校友林中以个人或班级名义冠名树木一棵，学校将镌刻有认养者信息的纪念牌安置于树木醒目处以示纪念。如果以班级名义捐赠，捐赠班级应推选联络人两名，以便校友总会联系。目前校友林认捐树木已有45棵。<br /><br />
 <strong>&nbsp;&nbsp;&nbsp;&nbsp;“紫气东来”门捐赠留念</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;上海交通大学“紫气东来”门为京宫式大门，位于闵行校区东西主轴线的东部起点，认捐额每人5000元人民币。学校将在东大门校园内广场北侧交通大学创始人盛宣怀铜像后的校史壁上篆刻捐赠者姓名，以示纪念，并颁发捐赠证书。目前东大门照壁留名校友已有148人。

