                                <h2>校友会章程</h2>
                                <li  style=" list-style:none;background:url(image/hr.gif) repeat-x left bottom; ">
								<p style="font-family:'宋体'; font-size:12px; margin-top:10px;">
                                <p>第一章 总 则 </p>

                          <p>第一条 本会定名"交通大学校友总会"（英文译名为：Jiao Tong University Alumni Association）。 <br>

        本章程所指交通大学为：上海交通大学、西安交通大学、西南交通大学、北方交通大学、新竹交通大学。 <br>

        本会是从事交通大学校友联谊工作的社会团体。 <br>

        <br>

        第二条 本会宗旨是：加强校友与母校、校友之间的联系、团结和合作，发扬交大的优良传统，共同为振兴中华，为增进各国人民之间的友谊，为母校的发展贡献力量。 </p>

                          <p>第三条 本会的任务是：沟通校友与母校、校友之间的联系，发挥海内外校友的智力优势和广泛影响，为母校的发展做贡献。 <br>

                          </p>

                          <p>第二章 会 员 </p>

                          <p>第四条 凡符合下列条件之一者均属交通大学校友： <br>

        一、 交通大学各个时期的毕业生、肄业生、进修生及各国留学生； <br>

        二、 曾在交通大学任教、任职者； <br>

        三、 交通大学聘请的兼职教授、访问教授、顾问教授、名誉教授及其他兼职人员等。 </p>

                          <p>第五条 凡交通大学校友，经与母校校友联络处或各地校友会取得联系，填写登记表，承认本会章程，即为本会会员。 </p>

                          <p>第六条 会员的权利和义务 <br>

        权利： <br>

        一、 在产生校友会各种机构成员时，有推举权和被推举权； <br>

        二、 参加校友会组织的各项活动并提出建议或意见； <br>

        三、 对母校的各项工作提出建议或意见； <br>

        四、 有限取得本会的有关刊物和资料。 <br>

        义务： <br>

        一、 遵守校友会章程； <br>

        二、 关心校友会的工作，参加校友会组织的各项活动； <br>

        三、 关心和支持母校的发展，促进国内外学术交流； <br>

        四、 积极向交大校友会刊物投送稿件。 <br>

        <br>

        第三章 组织机构 </p>

                          <p>第七条 总会设理事会，由理事若干人组成。每届理事会开会一至两次，轮流在各母校所在地召开。临时会议由会长决定召集之。 </p>

                          <p>第八条 总会理事由各地校友会协商推举产生，名额按地区分配。每届理事任期两年，可以连任。 </p>

                          <p>第九条 为加强与国外校友联系，特设通讯理事。通讯理事请国外各地交通大学校友会的会长或推举一名校友出任。 </p>

                          <p>第十条 为发挥老校友的作用，总会设名誉理事若干名，名誉理事从年时已高的原任理事中选任。 </p>

                          <p>第十一条 理事会职责 <br>

        一、 对外代表总会； <br>

        二、 推举会长、副会长，商定下届理事会理事名额分配； <br>

        三、 讨论和决定总会工作的方针、计划及重大问题； <br>

        四、 解释和修改《交通大学校友总会章程。 </p>

                          <p>第十二条　交通大学的校长为当然副会长，并按理事会换届年限轮流担任会长，主持理事会工作，另从理事中推举产生若干名副会长。 </p>

                          <p>第十三条　总会设总干事若干人，组成总干事会。总干事会对会长负责。负责总会日常工作，任期两年。总干事由各母校现任校长办公室主任或相当于这一职级的干部担任。总干事为当然理事。 </p>

                          <p>第十四条　各母校设校友联络处。联络处可由校长办公室、外事处和有关部门的同志组成。在主管校长的支持下，负责协调和联络海内外的校友，把国内校友会工作与海外校友联络工作紧密结合。 <br>

        一、 处理校友、各地校友会与总会联系事宜及日常工作；负责接待国内和海外返校校友讲学、参观访问等事宜；编辑《交大校友》和总会有关刊物。 <br>

        二、 《交大校友》由各母校派人组成编委会，其校长担任会长的学校任主编，负责编辑，出版发行工作。 </p>

                          <p>第十五条 在校友集中的地区，可成立各地校友会，名称统一称"交通大学ww校友会"（如交通大学北京校友会）；按照实际情况，在各地校友会下，可采取灵活、方便、多样的组织形式，更有效地开展校友工作。总会与各地区校友会建立密切的联系，但不存在组织上的隶属关系。 </p>

                          <p>第四章 经 费 <br>

                              <br>

        第十六条 总会经费由各母校资助、校友捐赠和以其他方式筹集。 <br>

        <br>

        第五章 终 止 <br>

        <br>

        第十七条 本会在遇到不可抗力的困难等原因必须终止活动时，由理事会全体会议表决，同意终止，并由会长签署有关文件后，即可终止本会活动。 </p>

                          <p>第六章 附 则 <br>

                              <br>

        第十八条 本章程经1997年5月16日校友总会第六届理事会通过施行。 </p>
                                 </p>
